package radioactivity.story;

public class InitialStory {
    public static String storyPrologue() {
        return """
                 Na cidade de pacata de Chernoville, eventos estranhos começaram a acontecer,
                 como ataques de animais mutantes saindo dos esgotos e atacando cidadãos que ali vivem,
                 e até mesmo doenças repentinas e inexplicáveis surgindo. Um detetive aposentado tem a missão
                 de combater tais monstros e encontrar a origem do problema.
                """;
    }

    public static String storyBeginning() {
        return """
                 Seu Abílio tomava café matinal e lia um jornal como de costume em uma tranquila
                 manhã de quarta-feira na cidade de Chernoville. De forma inesperada seu telefone tocou,
                 fato incomum, já que Seu Abílio não costumava receber muitas ligações,
                 o telefonema era de uma vizinha, que o contava desesperadamente que "animais estranhos"
                 haviam aparecido em uma rua próxima e que eles estavam atacando as pessoas que passavam por lá.
                 De primeira impressão Seu Abílio não compreendeu muito bem o que estava acontecendo,
                 ficando confuso com o que sua vizinha quis dizer com "animais estranhos",
                 porém como ele era um detetive aposentado conhecido e que possuía a confiança da vizinhança,
                 então era de praste que sempre pedissem a sua ajuda com qualquer problema que aparecia pelas redondezas,
                 portanto ele não estranhou essa ligação de ajuda e resolveu procurar por esses animais.
                 Ao chegar na rua indicada por sua vizinha, ele encontrou algo que seus olhos
                 nunca haviam vislumbrado antes, literalmente ratos, gatos, e outros bichos enormes, desfigurados,
                 com bolhas de pus ao redor do corpo, cheios de mutações. O detetive aposentado logo de cara notou
                 que havia algo de errado acontecendo, e que aquilo poderia ser um caso muito maior e mais perigoso
                 do que parecia, mas primeiro, ele precisava dar um jeito nas criaturas que se encontravam na sua frente.
                """;
    }
}
